const StringList = ({ items, icon: Icon, color }: { items: string[] | null; icon: typeof Trophy; color: string }) => {
  if (!items || items.length === 0) return null;
  const colorMap: Record<string, string> = {
    green: 'text-emerald-400 bg-emerald-500/10',
    red: 'text-red-400 bg-red-500/10',
    blue: 'text-blue-400 bg-blue-500/10',
  };
  return (
    <div className="space-y-1">
      {items.map((item, i) => (
        <div key={i} className="flex items-start gap-2 text-xs text-zinc-300">
          <div className={`w-5 h-5 rounded shrink-0 flex items-center justify-center ${colorMap[color] || colorMap.blue}`}>
            <Icon className="w-3 h-3" />
          </div>
          <p className="line-clamp-2">{item}</p>
        </div>
      ))}
    </div>
  );
};

