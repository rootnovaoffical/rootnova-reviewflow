import { useState, useEffect, useCallback } from 'react';
import { supabase } from '../lib/supabase';
import { useToast } from '../context/ToastContext';
import { LoadingSpinner, EmptyState, PageHeader, Card, Badge } from '../components/UI';
import {
  Sparkles, Zap, FileText, BarChart3, TrendingUp, AlertTriangle,
  Lightbulb, Trophy, Target, Clock,
} from 'lucide-react';

/* ============================================================
 * Shared helpers
 * ========================================================== */

const statusColor = (s: string | null): string => {
  switch (s) {
    case 'active': case 'accepted': case 'approved': case 'implemented': return 'green';
    case 'pending': case 'proposed': case 'open': return 'blue';
    case 'dismissed': case 'rejected': case 'closed': return 'gray';
    case 'failed': case 'overdue': return 'red';
    default: return 'gray';
  }
};

const confidenceColor = (c: number | null): string => {
  if (c == null) return 'gray';
  if (c >= 0.8) return 'green';
  if (c >= 0.5) return 'yellow';
  return 'red';
};

const fmtPct = (c: number | null): string => (c != null ? `${Math.round(c * 100)}%` : '—');
const fmtDate = (s: string | null) => (s ? new Date(s).toLocaleDateString() : '—');

/* ============================================================
 * AiRecommendationsModule
 * ========================================================== */

interface AiRecommendation {
  id: string;
  title: string | null;
  description: string | null;
  category: string | null;
  confidence: number | null;
  status: string | null;
  business_impact: string | null;
}

export function AiRecommendationsModule({ businessId }: { businessId: string }) {
  const { showToast } = useToast();
  const [items, setItems] = useState<AiRecommendation[]>([]);
  const [loading, setLoading] = useState(true);

  const fetchData = useCallback(async () => {
    setLoading(true);
    const { data, error } = await supabase
      .from('ai_recommendations')
      .select('id, title, description, category, confidence, status, business_impact')
      .eq('business_id', businessId)
      .order('created_at', { ascending: false });
    if (error) {
      showToast('error', 'Failed to load recommendations');
      setItems([]);
    } else {
      setItems((data ?? []) as AiRecommendation[]);
    }
    setLoading(false);
  }, [businessId, showToast]);

  useEffect(() => { fetchData(); }, [fetchData]);

  return (
    <div>
      <PageHeader title="AI Recommendations" description="Strategic recommendations generated by the AI engine." />
      {loading ? (
        <LoadingSpinner label="Loading recommendations…" />
      ) : items.length === 0 ? (
        <EmptyState icon={Sparkles} title="No recommendations" description="AI recommendations will appear here as they are generated." />
      ) : (
        <div className="grid gap-3 sm:grid-cols-2 lg:grid-cols-3">
          {items.map((r) => (
            <Card key={r.id} className="p-4 flex flex-col gap-2">
              <div className="flex items-start gap-2">
                <div className="w-8 h-8 rounded-lg bg-violet-500/15 flex items-center justify-center shrink-0">
                  <Sparkles className="w-4 h-4 text-violet-300" />
                </div>
                <p className="text-sm font-semibold text-white">{r.title || 'Untitled'}</p>
              </div>
              {r.description && <p className="text-xs text-zinc-400 line-clamp-3">{r.description}</p>}
              <div className="flex flex-wrap items-center gap-1.5">
                {r.category && <Badge color="purple">{r.category}</Badge>}
                <Badge color={confidenceColor(r.confidence)}>Confidence {fmtPct(r.confidence)}</Badge>
                <Badge color={statusColor(r.status)}>{r.status ?? '—'}</Badge>
              </div>
              {r.business_impact && (
                <div className="flex items-start gap-1.5 text-xs text-zinc-400 border-t border-white/5 pt-2 mt-auto">
                  <TrendingUp className="w-3.5 h-3.5 text-emerald-400 shrink-0 mt-0.5" />
                  <p className="line-clamp-2">{r.business_impact}</p>
                </div>
              )}
            </Card>
          ))}
        </div>
      )}
    </div>
  );
}

